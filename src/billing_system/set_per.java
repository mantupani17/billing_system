    public void removePerData()//remove the percentage information
    {
         int res=JOptionPane.showConfirmDialog(rootPane, "Do you Want To Delete ?", "Remove Percentage.", JOptionPane.YES_NO_OPTION);
         if(res==JOptionPane.YES_OPTION)
         {
          try{
              Statement pst=con.createStatement();
              int x=pst.executeUpdate("delete  from Per_tab where cname='"+(String)jComboBox1.getSelectedItem()+"' and pname='"+(String)jComboBox2.getSelectedItem()+"'");
              if(x!=0)
              {
                  JOptionPane.showMessageDialog(rootPane, "Delete Successfully...!", "Remove Percentage.", JOptionPane.INFORMATION_MESSAGE);
              }
              else
              {
                  JOptionPane.showMessageDialog(rootPane, "Record Not Found...!", "Remove Percentage.", JOptionPane.ERROR_MESSAGE);
              }
          }catch(Exception e)
          {
              JOptionPane.showMessageDialog(rootPane,  e.toString(),"Remove Percentage.", JOptionPane.ERROR_MESSAGE);
          }
             
         }
    }
