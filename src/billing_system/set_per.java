    public void setconnect(String uname,String pass) //this method for get a connection
    {
        try{
            String ser="jdbc:oracle:thin:@localhost:1521:XE";
            con=DriverManager.getConnection(ser, uname, pass);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
    public void setPercentage()//this method for set Percentage to the user
    {
        try{
            String sql="insert into per_tab values(?,?,?)";
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1, (String)jComboBox1.getSelectedItem());
            pst.setString(2, (String)jComboBox2.getSelectedItem());
            pst.setDouble(3, Double.parseDouble(jTextField1.getText()));
            int x=pst.executeUpdate();
            if(x>0)
            {
                JOptionPane.showMessageDialog(rootPane, "Percentage set For The customer:- "+(String)jComboBox1.getSelectedItem()+" ...!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
    public void setCustomer()//set Customer Names...!
    {
        try{
            ResultSet res=con.createStatement().executeQuery("select Cname from CUSTOMER_TAB");
            while(res.next())
            {
                jComboBox1.addItem(res.getString("Cname"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
     public void setProduct()//set Product Names...!
    {
        try{
            ResultSet res=con.createStatement().executeQuery("select Pname from PURCHASE_TAB");
            while(res.next())
            {
                jComboBox2.addItem(res.getString("Pname"));
            }
            res.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
     public void setColumn()
     {
         try{
         col=new Vector();
         row=new Vector();
         row.clear();
         col.add("Sl/no");
         col.add("Customer Name");
         col.add("Product name");
         col.add("Percentage");
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(rootPane, e.toString());
         }
     }
     public void viewPerData()//view All the percentage information for a customer
     {
      try{
          setColumn();
          Vector v=null;
          int x=1;
          Statement stm=con.createStatement();
            ResultSet res=stm.executeQuery("select * from per_tab");
            while(res.next())
            {
                v=new Vector();
                v.add(x++);
                v.add(res.getString("Cname"));
                v.add(res.getString("Pname"));
                v.add(res.getDouble("per"));
                row.add(v);
            }
            jTable1.setModel(new DefaultTableModel(row,col));
            res.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }   
     }
     public void updateData()//update the percentage
     {
         try{
             String cname=(String)jComboBox1.getSelectedItem();
             String pname=(String)jComboBox2.getSelectedItem();
             String sql="update per_tab set per="+jTextField1.getText()+" where Cname='"+cname+"' and pname='"+pname+"'";
             Statement stm=con.createStatement();
             int x=stm.executeUpdate(sql);
             if(x>0){
                 JOptionPane.showMessageDialog(rootPane, "Percentage Updated...!");
             }
         }catch(Exception e){
             JOptionPane.showMessageDialog(rootPane, e.toString());
         }
     }
